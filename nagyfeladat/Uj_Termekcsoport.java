    private void jTF_tcskKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTF_tcskKeyTyped
        char c = evt.getKeyChar();
        if (jTF_tcsk.getText().equals(""))
        {
           if ( ((c < '1') || (c > '9')) && (c != KeyEvent.VK_BACK_SPACE)) 
           {
                evt.consume();  // ha nem szám, akkor ignorálja
           } 
        }
        else
        {
            if ( ((c < '0') || (c > '9')) && (c != KeyEvent.VK_BACK_SPACE)) 
            {
                evt.consume();  // ha nem szám, akkor ignorálja
            } 
        }  
    }//GEN-LAST:event_jTF_tcskKeyTyped

