    private void jB_FelvetelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_FelvetelActionPerformed
        jL_info.setText("");
        boolean ok=true;
        if (ok)
        {
            if (jTF_tcsk.getText().equals("") || jTF_tcsn.getText().equals(""))
            {
                jL_info.setText("Ne hagyjon üres mezőket!");
                ok=false;
            }
            else
            {
                conn=EnSqlkapcsolatom.ConnectDB();
                String sql="Select * from Termekcsoport where Tcs_kod='"+jTF_tcsk.getText()+"'";
                try
                {
                    Statement sta = conn.createStatement();
                    ResultSet rs= sta.executeQuery(sql);
                    if (rs.next())
                    {
                       Nevellenorzes=rs.getString("Tcs_kod");
                       System.out.println(Nevellenorzes);
                       jL_info.setText("Ez a kód már szerepel!");
                       ok=false; 
                    }
                    sta.close();
                    conn.close(); 
                }
                catch(Exception e)
                {
                        JOptionPane.showMessageDialog(null,e);
                }
                conn=EnSqlkapcsolatom.ConnectDB();
                sql="Select * from Termekcsoport where Tcs_nev='"+jTF_tcsn.getText()+"'";
                try
                {
                    Statement sta = conn.createStatement();
                    ResultSet rs= sta.executeQuery(sql);
                    if (rs.next())
                    {
                       Nevellenorzes=rs.getString("Tcs_nev");
                       System.out.println(Nevellenorzes);
                       jL_info.setText("Ez a termekcsoport már létezik!");
                       ok=false; 
                    }
                    sta.close();
                    conn.close(); 
                }
                catch(Exception e)
                {
                        JOptionPane.showMessageDialog(null,e);
                }
            }
        }
        if (ok)
        {
            try
                {
                    conn=EnSqlkapcsolatom.ConnectDB();
                    String sqlin="INSERT INTO Termekcsoport (Tcs_kod, Tcs_nev) VALUES "
                     +"('"+jTF_tcsk.getText()+"',"
                     +"'"+jTF_tcsn.getText()+"')";
                    Statement st=conn.createStatement();
                    st.executeUpdate(sqlin);         
                    JOptionPane.showMessageDialog(null, "Insert megtörtént!");
                    st.close();
                    conn.close(); 
                    this.dispose();
                }
                catch(Exception e)
                {
                    System.out.println("Hiba"+e);
                    JOptionPane.showMessageDialog(null, "Sikertelen felvétel!");
                }
        }
    }//GEN-LAST:event_jB_FelvetelActionPerformed

